function compareMaterials(left, right) {
  const leftCategoryPriority = Object.prototype.hasOwnProperty.call(MATERIAL_CATEGORY_PRIORITY, left.category)
    ? MATERIAL_CATEGORY_PRIORITY[left.category]
    : 99
  const rightCategoryPriority = Object.prototype.hasOwnProperty.call(MATERIAL_CATEGORY_PRIORITY, right.category)
    ? MATERIAL_CATEGORY_PRIORITY[right.category]
    : 99

  if (leftCategoryPriority !== rightCategoryPriority) {
    return leftCategoryPriority - rightCategoryPriority
  }

  const leftIdPriority = Object.prototype.hasOwnProperty.call(MATERIAL_ID_PRIORITY, left.id)
    ? MATERIAL_ID_PRIORITY[left.id]
    : 99
  const rightIdPriority = Object.prototype.hasOwnProperty.call(MATERIAL_ID_PRIORITY, right.id)
    ? MATERIAL_ID_PRIORITY[right.id]
    : 99

  if (leftIdPriority !== rightIdPriority) {
    return leftIdPriority - rightIdPriority
  }

  return left.title.localeCompare(right.title, 'zh-CN')
}

function getMaterials() {
  return MATERIALS.slice().sort(compareMaterials)
}

function getMaterialById(id) {
  const normalizedId = String(id || '').trim()
  if (!normalizedId) {
    return undefined
  }
  let decodedId = normalizedId
  try {
    decodedId = decodeURIComponent(normalizedId)
  } catch (error) {
    decodedId = normalizedId
  }
  return MATERIALS.find((item) => item.id === normalizedId || item.id === decodedId)
}

